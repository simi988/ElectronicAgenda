package model;

public class Calculator {
	public double adunare(double x, double y) {
		return x + y;
	}

	public double scadere(double x, double y) {
		return x - y;
	}

	public double inmultire(double x, double y) {
		return x * y;
	}

	public double impartire(double x, double y) {
		return x / y;
	}
	
}
